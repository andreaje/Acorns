def t(key: str, lang: str = "en") -> str:
    return TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, TRANSLATIONS["en"].get(key, key))


def localize_coach_response(text: str, lang: str) -> str:
    if lang == "en":
        return text
    localized = text
    for source, translated in sorted(COACH_TRANSLATIONS.get(lang, {}).items(), key=lambda item: len(item[0]), reverse=True):
        localized = localized.replace(source, translated)
    return localized
